package bean;

/**
 *
 * @author Plutonianaus
 */
public class UTMAuthority {
    private String AuthoEmail;
    private String AuthoID;
    private String AuthoName;

    /**
     * @return the AuthoEmail
     */
    public String getAuthoEmail() {
        return AuthoEmail;
    }

    /**
     * @param AuthoEmail the AuthoEmail to set
     */
    public void setAuthoEmail(String AuthoEmail) {
        this.AuthoEmail = AuthoEmail;
    }

    /**
     * @return the AuthoID
     */
    public String getAuthoID() {
        return AuthoID;
    }

    /**
     * @param AuthoID the AuthoID to set
     */
    public void setAuthoID(String AuthoID) {
        this.AuthoID = AuthoID;
    }

    /**
     * @return the AuthoName
     */
    public String getAuthoName() {
        return AuthoName;
    }

    /**
     * @param AuthoName the AuthoName to set
     */
    public void setAuthoName(String AuthoName) {
        this.AuthoName = AuthoName;
    }
    
}
